package com.example.newapp;

public class EquipmentBean {
    private String equip_name;
    private String equip_pic;
    private String equip_intro;

    public String getEquip_name() {
        return equip_name;
    }

    public void setEquip_name(String equip_name) {
        this.equip_name = equip_name;
    }

    public String getEquip_pic() {
        return equip_pic;
    }

    public void setEquip_pic(String equip_pic) {
        this.equip_pic = equip_pic;
    }

    public String getEquip_intro() {
        return equip_intro;
    }

    public void setEquip_intro(String equip_intro) {
        this.equip_intro = equip_intro;
    }
}
